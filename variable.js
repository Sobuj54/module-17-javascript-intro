// vary +  able = variable
// variable declaration : var variable-name = value;
// here var is a keyword for variable in javascript.
var price = 100
var weight = 30
var fair = 28
var temparature = 22

// strings as variables
// we need to use double quotation for declaring string varibles
var address = 'Dhanmondi-15'
var name = 'sobuj'
// single quotes can also be used to declare string variables but we can not use both at same string.
var workplace = 'dhaka'

// boolean
// boolean varibles dont need quotation because they are keywords
var exampassed = true
var isgone = false
